package database

import (
	"database/sql"
	"fmt"
)

// OpenDB открывает пул соединений с базой данных SQLite по указанному адресу (dsn).
// Функция возвращает готовый к работе объект *sql.DB или ошибку, если подключение невозможно.
func OpenDB(dsn string) (*sql.DB, error) {
	// 1. Инициализация пула соединений
	// sql.Open НЕ создает реального подключения к базе данных прямо сейчас.
	// Он лишь проверяет, зарегистрирован ли драйвер "sqlite", валидирует синтаксис строки dsn
	// и выделяет память под структуру пула соединений (*sql.DB).
	db, err := sql.Open("sqlite", dsn)
	if err != nil {
		return nil, err // Если имя драйвера неверное или синтаксис dsn сломан, возвращаем ошибку.
	}

	// 2. Фактическая проверка связи
	// Так как sql.Open ленив, мы принудительно вызываем db.Ping().
	// Этот метод заставляет драйвер физически открыть файл SQLite (или создать новое соединение).
	if err := db.Ping(); err != nil {
		// Защита от утечки ресурсов: если Ping не прошел, пул соединений всё равно остался инициализирован в памяти.
		// Мы обязаны вручную вызвать db.Close(), чтобы очистить выделенные под него ресурсы.
		db.Close()

		// Возвращаем ошибку, оборачивая её через %w, чтобы сохранить оригинальную причину сбоя.
		return nil, fmt.Errorf("проверка ping не удалась: %w", err)
	}

	// 3. Успешное завершение
	// Если Ping прошел без ошибок, база данных гарантированно доступна и готова к работе.
	return db, nil
}
